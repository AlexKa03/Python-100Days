# Subscripting
print("Hello"[0])
print("Hello"[-1]) # it is = to last index of the string

# String
print("Hello")
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123456789)
print(123_456_789) # Benefit is only visual and it is for us only

# Float
print(3.14159)

# Boolean
print(True)
print(False)
